import { promises as fs } from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const PUBLIC_DIR = path.join(ROOT, "public");
const OUTPUT_FILE = path.join(ROOT, "netlify/functions/generated/site-knowledge.mts");
const INCLUDED_EXTENSIONS = new Set([".html", ".json"]);
const MAX_CHUNK_LENGTH = 1800;
const CHUNK_OVERLAP = 180;

const ENTITY_MAP = {
  amp: "&",
  apos: "'",
  gt: ">",
  hellip: "…",
  laquo: "«",
  ldquo: "“",
  lsquo: "‘",
  lt: "<",
  mdash: "—",
  middot: "·",
  nbsp: " ",
  ndash: "–",
  quot: '"',
  raquo: "»",
  rdquo: "”",
  rsquo: "’",
};

async function listFiles(directory) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await listFiles(fullPath)));
    } else if (INCLUDED_EXTENSIONS.has(path.extname(entry.name).toLowerCase())) {
      files.push(fullPath);
    }
  }

  return files.sort();
}

function decodeEntities(value) {
  return value.replace(/&(#x?[\da-f]+|[a-z]+);/gi, (match, entity) => {
    if (entity[0] === "#") {
      const hexadecimal = entity[1]?.toLowerCase() === "x";
      const codePoint = Number.parseInt(entity.slice(hexadecimal ? 2 : 1), hexadecimal ? 16 : 10);
      return Number.isFinite(codePoint) ? String.fromCodePoint(codePoint) : match;
    }

    return ENTITY_MAP[entity.toLowerCase()] ?? match;
  });
}

function removePersonalName(value) {
  return value.replace(/\b(?:Victor(?:\s+Gregg)?(?:\s+Hale)?|Gregg\s+Hale|Hale)\b/gi, "the owner");
}

function cleanText(value) {
  return removePersonalName(decodeEntities(value))
    .replace(/\r/g, "")
    .replace(/[ \t]+/g, " ")
    .replace(/ *\n */g, "\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function extractMeta(html, expression) {
  const match = html.match(expression);
  return cleanText(match?.[1] ?? "");
}

function extractHtml(html) {
  const title = extractMeta(html, /<title[^>]*>([\s\S]*?)<\/title>/i);
  const description = extractMeta(
    html,
    /<meta[^>]+name=["']description["'][^>]+content=["']([^"']*)["'][^>]*>/i,
  );
  const withoutChrome = html
    .replace(/<!--[\s\S]*?-->/g, " ")
    .replace(/<(script|style|noscript|svg|template)\b[^>]*>[\s\S]*?<\/\1>/gi, " ")
    .replace(/<(nav|footer)\b[^>]*>[\s\S]*?<\/\1>/gi, " ")
    .replace(/<[^>]+>/g, "\n");

  const body = cleanText(withoutChrome)
    .split("\n")
    .filter((line, index, lines) => line && line !== lines[index - 1])
    .join("\n");

  return { title, text: cleanText([description, body].filter(Boolean).join("\n\n")) };
}

function extractJson(raw, relativePath) {
  const parsed = JSON.parse(raw);
  const title = relativePath.endsWith("services.json")
    ? "AAA Handyman Services service catalog"
    : "AAA Handyman Services service areas";
  return { title, text: cleanText(JSON.stringify(parsed, null, 2)) };
}

function routeFor(relativePath) {
  const normalized = relativePath.split(path.sep).join("/");
  if (normalized === "index.html") return "/";
  if (normalized.endsWith(".html")) return `/${normalized.slice(0, -5)}`;
  return `/${normalized}`;
}

function chunkText(text) {
  if (text.length <= MAX_CHUNK_LENGTH) return [text];

  const paragraphs = text.split(/\n{2,}/).flatMap((paragraph) => {
    if (paragraph.length <= MAX_CHUNK_LENGTH) return [paragraph];
    return paragraph.match(new RegExp(`.{1,${MAX_CHUNK_LENGTH}}(?:\\s|$)`, "gs")) ?? [paragraph];
  });
  const chunks = [];
  let current = "";

  for (const paragraph of paragraphs) {
    const candidate = current ? `${current}\n\n${paragraph}` : paragraph;
    if (candidate.length <= MAX_CHUNK_LENGTH) {
      current = candidate;
      continue;
    }

    if (current) chunks.push(current.trim());
    const overlap = current.slice(-CHUNK_OVERLAP).replace(/^\S*\s/, "");
    current = overlap ? `${overlap}\n\n${paragraph}` : paragraph;
  }

  if (current) chunks.push(current.trim());
  return chunks.filter(Boolean);
}

const files = await listFiles(PUBLIC_DIR);
const pages = [];

for (const filePath of files) {
  const relativePath = path.relative(PUBLIC_DIR, filePath);
  const raw = await fs.readFile(filePath, "utf8");
  const extracted = path.extname(filePath).toLowerCase() === ".html"
    ? extractHtml(raw)
    : extractJson(raw, relativePath);

  chunkText(extracted.text).forEach((text, chunkIndex) => {
    pages.push({
      path: routeFor(relativePath),
      title: extracted.title || routeFor(relativePath),
      chunk: chunkIndex + 1,
      text,
    });
  });
}

const output = `// Generated by scripts/build-chat-knowledge.mjs. Do not edit manually.\nexport const siteKnowledge = ${JSON.stringify(pages, null, 2)} as const;\n`;
await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
await fs.writeFile(OUTPUT_FILE, output);

console.log(`Indexed ${files.length} site files into ${pages.length} knowledge chunks.`);
